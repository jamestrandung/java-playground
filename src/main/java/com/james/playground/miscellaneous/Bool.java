package com.james.playground.miscellaneous;

public class Bool {
  public static void main(String[] args) {
    Boolean value = null;
    System.out.println(Boolean.TRUE.equals(value));
    System.out.println(Boolean.FALSE.equals(value));

    Object value2 = null;
    System.out.println(Boolean.TRUE.equals(value2));
    System.out.println(Boolean.FALSE.equals(value2));
  }
}
